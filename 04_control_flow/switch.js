const month = "mar"

switch (month) {
    case "jan":
        console.log("Jan");        
        break;
    case "feb":
        console.log("Feb");        
        break;
    case "mar":
        console.log("Mar");        
        break;
    case "apr":
        console.log("Apr");        
        break;
    case "may":
        console.log("May");        
        break;
    
    default:
        console.log("default case match");
        
        break;
}

// if break;  statement is not there or commented
// control executes all statements after matching condition
// except the default condition 
